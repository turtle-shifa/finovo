    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        //
        $request->validate([
        'email' => 'required',
        'password' => 'required'
        ]);

        $user = User::where('email', $request->email)->first();
        if ($user && Hash::check($request->password, $user->password)) {
            session(['user_id' => $user->id]);
            $hasCompany = Company::where('user_id','=',$user->id)->first();
            if ($hasCompany){
                return redirect('/dashboard')->with('success', 'Log in successfully!');
            }else{
                return redirect('/company-create')->with('success', 'Log in successfully!');
            }

        } else {
            return back()->with('error', 'The email address or password you entered is incorrect. Please try again.');
        }
    }

